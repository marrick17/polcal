void ftoa(double f, char *s){

  char s_hid[5]; /*for storing the fractional part of the string*/

  int integer_part = (int)f;
  double frac_part = f - integer_part;
  itoa(integer_part, s);
  
  if(frac_part == 0.0){
    return;
  }

  strcat(s,".");
 
  int frac_part_adj = (int)((frac_part)*1000); /*3 decimal places*/
  itoa(frac_part_adj, s_hid);

  strcat(s, s_hid);
  
}
