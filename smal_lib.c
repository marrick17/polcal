void nreverse (char *s){
  
  char *s_begin = &s[0];
  char *s_end;
  char temp;
  
  for(s_end = &s[0]; *s_end != '\0'; s_end++){
    ;
  }
  
  for(--s_end ; s_begin < s_end; s_begin++, s_end-- ){
    
    temp = *s_begin;
    *s_begin = *s_end;
    *s_end = temp;
  }
}
