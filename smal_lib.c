int ngetline(char *line, int max){

  int c;

  char *pline_begin = &line[0]; 

  for(c = getchar(); (max--) -2 >= 0  && c != EOF && c != '\n'; c =  getchar(), line++ ){

    *line = c;

  }
  
  if (c == '\n'){
    *line++ = c;
  }

  *line = '\0';

  return line - pline_begin;

}
